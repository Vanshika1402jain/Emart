    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null, "Please fill all the fields!","Incomplete data!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            EmployeesPojo emp=new EmployeesPojo();
            emp.setEmpid(txtempid.getText());
            emp.setEmpname(txtename.getText());
            emp.setJob(cjob.getSelectedItem().toString());
            emp.setSalary(Double.parseDouble(txtsalary.getText()));
        
            boolean response=EmployeesDao.addEmployee(emp);
            if(response){
            JOptionPane.showMessageDialog(null, "Record Added successfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
            clearText();
            loadEmpId();
            }
            else{
            JOptionPane.showMessageDialog(null, "Could Not insert data!","Try Again!",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException ex){
          JOptionPane.showMessageDialog(null, "Please Fill Numeric Data!","Error!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
        } 
        catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
        }
    }//GEN-LAST:event_btnaddActionPerformed

