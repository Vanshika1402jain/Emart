 private void getOrders() {
        try{
            jcoid.removeAllItems();
            List<String>o=OrderDao.getAllId();
            for(String id:o){
                jcoid.addItem(id);
            }
        }
        catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
        }
    }
